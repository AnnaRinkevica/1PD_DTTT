    private void jMenu9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu9MouseClicked
        TestList.setBounds(0, 0, 600, 310);
        TestList.setLocationRelativeTo(null);
        TestList.setVisible(true);
        CreateDate.dispose();
        if (user.getRights()) {
            jButton11.setVisible(true);
            jButton12.setVisible(true);
            jButton13.setVisible(true);
        } else {
            jButton11.setVisible(false);
            jButton12.setVisible(false);
            jButton13.setVisible(false);
        }
        DefaultListModel model = new DefaultListModel();
        for (Test test : user.getTestList()) {
            if (test.isActive() || user.getRights()) {
                model.addElement(test.getName());
            }
        }
        jList1.setModel(model);

        if (model.isEmpty()) {
            Exception5.setBounds(0, 0, 353, 125);
            Exception5.setLocationRelativeTo(null);
            Exception5.setVisible(true);
        }
    }//GEN-LAST:event_jMenu9MouseClicked

