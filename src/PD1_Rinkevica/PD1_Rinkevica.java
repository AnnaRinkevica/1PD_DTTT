    public void signUp() {
        String login = jTextField3.getText();
        String password = new String(jPasswordField2.getPassword());
        String passwordCheck = new String(jPasswordField3.getPassword());
        if (jTextField3.getText().equals("") || new String(jPasswordField2.getPassword()).equals("") || new String(jPasswordField3.getPassword()).equals("")) {
            Exception2.setBounds(0, 0, 353, 125);
            Exception2.setLocationRelativeTo(null);
            Exception2.setVisible(true);
        } else if (!DB_User.isLoginExists(login)) {
            if (password.equals(passwordCheck)) {
                boolean rights = jComboBox1.getSelectedIndex() == 1;
                user = DB_User.addUser(login, password, rights);
                Log1.setBounds(0, 0, 353, 125);
                Log1.setLocationRelativeTo(null);
                Log1.setVisible(true);
            } else {
                Exception3.setBounds(0, 0, 353, 125);
                Exception3.setLocationRelativeTo(null);
                Exception3.setVisible(true);
            }
        } else {
            Exception4.setBounds(0, 0, 353, 125);
            Exception4.setLocationRelativeTo(null);
            Exception4.setVisible(true);
        }
    }

    public void login() {
        String login = writeLogin.getText();
        String password = new String(jPasswordField1.getPassword());

        if (writeLogin.getText().equals("") || new String(jPasswordField1.getPassword()).equals("")) {
            Exception2.setBounds(0, 0, 353, 125);
            Exception2.setLocationRelativeTo(null);
            Exception2.setVisible(true);
        } else if (DB_User.isLoginExists(login)) {
            if (DB_User.isPasswordCorrect(login, password)) {

                user = DB_User.getUser(login, password);
                jLabel9.setText(user.getLogin());
                jLabel11.setText(user.getPassword());
                jLabel13.setText(user.getFirstName());
                jLabel15.setText(user.getLastName());
                jLabel16.setText(user.getEmail());
                jLabel40.setText((user.getRights()) ? "Skolotājs" : "Skolēns");

                Profile.setBounds(0, 0, 650, 290);
                Profile.setLocationRelativeTo(null);
                Profile.setVisible(true);
                dispose();

            } else {
                Exception1.setBounds(0, 0, 353, 125);
                Exception1.setLocationRelativeTo(null);
                Exception1.setVisible(true);
            }
        } else {
            Exception1.setBounds(0, 0, 353, 125);
            Exception1.setLocationRelativeTo(null);
            Exception1.setVisible(true);
        }
    }

